const regexMd = {
    '**': /\*\*(.+?)\*\*/g,
    '__': /__(.+?)__/g,
    '*': /\*(.+?)\*/g,
    '_': /_(.+?)_/g,
    '`': /```[\s\S]+?```|`([^`]+)`/g,
    '```': /```([\s\S]*?)```/g,
    '#': /(#{1,6})\s?(.+)/g,
    '  ': /  \n/g,
    'link': /(?<!\!)\[([^\]]+)\]\(([^\)]+)\)/g,
    'img': /!\[([^\]]*)\]\(([^ ]+?)(?: "([^"]*)")?\)/g,
    'p': /^(?!<h[1-6]>|<pre>|<blockquote>)([^\n]+?)(?<!<\/pre>|<\/blockquote>)\n/gm,
}

export default regexMd;