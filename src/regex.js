const regexMd = {
    "**": /\*\*(.+?)\*\*/g,
    "__": /__(.+?)__/g,
    "*": /\*(.+?)\*/g,
    "_": /_(.+?)_/g,
    "`": /`([^`]+)`/g,
    "#": /(#{1,6})\s?(.+)/g,
    "  ": /  \n/g,
    "\n": /([^\n])(?!\n*<\/?h)\n([^\n])/g,
    "p": /^(?!<h[1-6]>)([^\n]+)\n/gm,
    "blockquote": /^>(.*(?:\n(?!>).*?)*)/gm
}

export default regexMd;